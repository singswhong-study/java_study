package exam.Math;

public class MathExam {
    public static void main(String[] args) {

        System.out.println(Math.max(1,4));  //최대값 계산 => 두 값중 큰것.
        System.out.println(Math.min(1,4));  //최소값 게산
        System.out.println(Math.abs(-1));   //절대값 계산 absolute
        System.out.println(Math.random());  //랜덤값 0.0 < result < 1.0
        System.out.println(Math.sqrt(25));  //제곱근 square root
        System.out.println(Math.pow(2,10)); //거듭제곱. power
    }
}
